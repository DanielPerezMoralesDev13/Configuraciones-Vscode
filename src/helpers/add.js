/**
 * Función: **Add 2 numbers together**
 * 
 * @param {number} a El primer número a sumar.
 * @param {number} b El segundo número a sumar.
 * @returns La suma de `a` y `b`.
 */
export default function add(a, b) {
    return a + b;
}

